     //--------------------------------------------------------------------------------------------------------
    // Intersect
    //--------------------------------------------------------------------------------------------------------
    //
    // @param ...args
    //
    //--------------------------------------------------------------------------------------------------------
    public static function do(...$args) : Array
    {
        return array_intersect(...$args);
    }

    //--------------------------------------------------------------------------------------------------------
    // Intersect Key
    //--------------------------------------------------------------------------------------------------------
    //
    // @param ...args
    //
    //--------------------------------------------------------------------------------------------------------
    public static function key(...$args) : Array
    {
        return array_intersect_key(...$args);
    }
}
