    //--------------------------------------------------------------------------------------------------------
    // Combine
    //--------------------------------------------------------------------------------------------------------
    //
    // @param array $keys
    // @param array $values
    //
    //--------------------------------------------------------------------------------------------------------
    public static function do(Array $keys, Array $values) : Array
    {
        return array_combine($keys, $values);
    }
}
