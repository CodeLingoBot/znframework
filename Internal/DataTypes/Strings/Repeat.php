    //--------------------------------------------------------------------------------------------------------
    // Repeat
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $str
    // @param numeric $count
    //
    //--------------------------------------------------------------------------------------------------------
    public static function do(String $string, Int $count = 1) : String
    {
        return str_repeat($string, $count);
    }
}
