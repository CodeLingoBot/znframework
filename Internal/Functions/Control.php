//--------------------------------------------------------------------------------------------------
// isImport()
//--------------------------------------------------------------------------------------------------
//
// @param string $path
//
// @return Bool
//
//--------------------------------------------------------------------------------------------------
function isImport(String $path) : Bool
{
    if( in_array( realpath(suffix($path, '.php')), get_required_files() ) )
    {
        return true;
    }
    else
    {
        return false;
    }
}

//--------------------------------------------------------------------------------------------------
// isUrl()
//--------------------------------------------------------------------------------------------------
//
// @param string $url
//
// @return Bool
//
//--------------------------------------------------------------------------------------------------
function isUrl(String $url) : Bool
{
    if( ! preg_match('#^(\w+:)?//#i', $url) )
    {
        return false;
    }
    else
    {
        return true;
    }
}

//--------------------------------------------------------------------------------------------------
// isEmail()
//--------------------------------------------------------------------------------------------------
//
// @param string $email
//
// @return Bool
//
//--------------------------------------------------------------------------------------------------
function isEmail(String $email) : Bool
{
    if( ! preg_match("/^([a-z0-9\+_\-]+)(\.[a-z0-9\+_\-]+)*@([a-z0-9\-]+\.)+[a-z]{2,6}$/ix", $email) )
    {
        return false;
    }
    else
    {
        return true;
    }
}

//--------------------------------------------------------------------------------------------------
// isChar()
//--------------------------------------------------------------------------------------------------
//
// @param mixed $str
//
// @return Bool
//
//--------------------------------------------------------------------------------------------------
function isChar($str) : Bool
{
    if( is_scalar($str) )
    {
        return true;
    }
    else
    {
        return false;
    }
}

//--------------------------------------------------------------------------------------------------
// isRealNumeric()
//--------------------------------------------------------------------------------------------------
//
// @param numeric $num = 0
//
// @return Bool
//
//--------------------------------------------------------------------------------------------------
function isRealNumeric($num = 0) : Bool
{
    if( ! is_string($num) && is_numeric($num) )
    {
        return true;
    }
    else
    {
        return false;
    }
}

//--------------------------------------------------------------------------------------------------
// isDeclaredClass()
//--------------------------------------------------------------------------------------------------
//
// @param string $class
//
// @return Bool
//
//--------------------------------------------------------------------------------------------------
function isDeclaredClass(String $class) : Bool
{
    if( in_array(strtolower($class), array_map('strtolower', get_declared_classes())) )
    {
        return true;
    }
    else
    {
        return false;
    }
}


//--------------------------------------------------------------------------------------------------
// isHash()
//--------------------------------------------------------------------------------------------------
//
// @param string $type
//
// @return Bool
//
//--------------------------------------------------------------------------------------------------
function isHash(String $type) : Bool
{
    if( in_array($type, hash_algos()) )
    {
        return true;
    }
    else
    {
        return false;
    }
}

//--------------------------------------------------------------------------------------------------
// isCharset()
//--------------------------------------------------------------------------------------------------
//
// @param string $charset
//
// @return Bool
//
//--------------------------------------------------------------------------------------------------
function isCharset(String $charset) : Bool
{
    if( array_search(strtolower($charset), array_map('strtolower', mb_list_encodings()), true) )
    {
        return true;
    }
    else
    {
        return false;
    }
}

//--------------------------------------------------------------------------------------------------
// isArray
//--------------------------------------------------------------------------------------------------
//
// @param mixed $array
//
// @return Bool
//
//--------------------------------------------------------------------------------------------------
function isArray($array) : Bool
{
    if( ! empty($array) && is_array($array) )
    {
        return true;
    }
    else
    {
        return false;
    }
}

//--------------------------------------------------------------------------------------------------
// Null Coalesce
//--------------------------------------------------------------------------------------------------
//
// @param var   &$var
// @param mixed $value
//
// @return void
//
//--------------------------------------------------------------------------------------------------
function nullCoalesce( & $var, $value)
{
    $var = $var === NULL ? $value : $var;
}
