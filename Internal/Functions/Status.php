//--------------------------------------------------------------------------------------------------
// sslStatus()
//--------------------------------------------------------------------------------------------------
//
// @param void
//
// @return string
//
//--------------------------------------------------------------------------------------------------
function sslStatus() : string
{
    return ! Config::get('Services','uri')['ssl']
           ? 'http://'
           : 'https://';
}

//--------------------------------------------------------------------------------------------------
// indexStatus()
//--------------------------------------------------------------------------------------------------
//
// @param void
//
// @return string
//
//--------------------------------------------------------------------------------------------------
function indexStatus() :? string
{
    return ! Config::get('Htaccess', 'uri')['directoryIndex']
           ? NULL
           : suffix(DIRECTORY_INDEX);
}
