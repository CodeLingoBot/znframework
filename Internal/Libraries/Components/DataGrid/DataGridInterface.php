	//----------------------------------------------------------------------------------------------------
	// Columns
	//----------------------------------------------------------------------------------------------------
	//
	// @param  array $columns -> Grid'de gösterilecek sütunlar
	// @return object
	//
	//----------------------------------------------------------------------------------------------------
	public function columns(Array $columns) : InternalDataGrid;
	
	//----------------------------------------------------------------------------------------------------
	// Process Columns
	//----------------------------------------------------------------------------------------------------
	//
	// @param  string $column   -> İşlem yapılacak sütun adı
	// @param  bool   $editable -> Bu sütunun düzenlenebilir olup olmayacağı
	// @return object
	//
	//----------------------------------------------------------------------------------------------------
	public function processColumn(String $column, Bool $editable = false) : InternalDataGrid;
	
	//----------------------------------------------------------------------------------------------------
	// Table
	//----------------------------------------------------------------------------------------------------
	//
	// @param  string $table -> İşlem yapılacak esas tablo
	// @return object
	//
	//----------------------------------------------------------------------------------------------------
	public function table(String $table) : InternalDataGrid;
	
	//----------------------------------------------------------------------------------------------------
	// Limit
	//----------------------------------------------------------------------------------------------------
	//
	// @param  numeric $limit -> Tek bir sayfada görüntülenecek kayıt sayısı.
	// @return object
	//
	//----------------------------------------------------------------------------------------------------
	public function limit(Int $limit = 20) : InternalDataGrid;
	
	//----------------------------------------------------------------------------------------------------
	// Order By
	//----------------------------------------------------------------------------------------------------
	//
	// @param  string $column -> Sıralama sütunu.
	// @param  string $order  -> Sıralama türü.
	// @return object
	//
	//----------------------------------------------------------------------------------------------------
	public function orderBy(String $column, String $order) : InternalDataGrid;
	
	//----------------------------------------------------------------------------------------------------
	// Group By
	//----------------------------------------------------------------------------------------------------
	//
	// @param  string $column -> Gruplama sütunu.
	// @return object
	//
	//----------------------------------------------------------------------------------------------------
	public function groupBy(String $column) : InternalDataGrid;
	
	//----------------------------------------------------------------------------------------------------
	// Where
	//----------------------------------------------------------------------------------------------------
	//
	// @param  array  args -> koşul oluşturmak için kullanılır.
	// @return object
	//
	//----------------------------------------------------------------------------------------------------
	public function where(...$args) : InternalDataGrid;
	
	//----------------------------------------------------------------------------------------------------
	// Table
	//----------------------------------------------------------------------------------------------------
	//
	// @param  array $tables -> Birleştirilecek tablolar dizisi
	// @return object
	//
	//----------------------------------------------------------------------------------------------------
	public function joins(Array $tables) : InternalDataGrid;
	
	//----------------------------------------------------------------------------------------------------
	// Create
	//----------------------------------------------------------------------------------------------------
	//
	// @param  void
	// @return string
	//
	//----------------------------------------------------------------------------------------------------
	public function create() : String;
}