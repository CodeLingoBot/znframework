	//----------------------------------------------------------------------------------------------------
	// Magic Call
	//----------------------------------------------------------------------------------------------------
	// 
	// @param string $method
	// @param array  $parameters
	//
	//----------------------------------------------------------------------------------------------------
	public function __call($method, $parameters)
	{
		$this->data = \Arrays::$method($this->data, ...$parameters);

		return $this;
	}

	//----------------------------------------------------------------------------------------------------
	// Protected Data
	//----------------------------------------------------------------------------------------------------
	// 
	// @var string
	//
	//----------------------------------------------------------------------------------------------------
	protected $data = [];
	
	//----------------------------------------------------------------------------------------------------
	// Data
	//----------------------------------------------------------------------------------------------------
	// 
	// @param string $data
	//
	//----------------------------------------------------------------------------------------------------
	public function data(Array $data)
	{
		$this->data = $data;
		
		return $this;
	}	

	//----------------------------------------------------------------------------------------------------
	// Get
	//----------------------------------------------------------------------------------------------------
	// 
	// @param  void
	// @return string
	//
	//----------------------------------------------------------------------------------------------------
	public function get()
	{
		$data = $this->data;
		
		$this->data = [];
		
		return $data;
	}
}