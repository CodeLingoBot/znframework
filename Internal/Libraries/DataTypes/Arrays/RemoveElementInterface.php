<?php namespace ZN\DataTypes\Arrays;

interface RemoveElementInterface
{
    //--------------------------------------------------------------------------------------------------------
    //
    // Author     : Ozan UYKUN <ozanbote@gmail.com>
    // Site       : www.znframework.com
    // License    : The MIT License
    // Copyright  : (c) 2012-2016, znframework.com
    //
    //--------------------------------------------------------------------------------------------------------

    //--------------------------------------------------------------------------------------------------------
    // Remove Key
    //--------------------------------------------------------------------------------------------------------
    //
    // @param array $array
    // @param mixed $keys
    //
    //--------------------------------------------------------------------------------------------------------
    public function key(array $array, $keys) : array;

    //--------------------------------------------------------------------------------------------------------
    // Remove Value
    //--------------------------------------------------------------------------------------------------------
    //
    // @param array $array
    // @param mixed $values
    //
    //--------------------------------------------------------------------------------------------------------
    public function value(array $array, $values) : array;

    //--------------------------------------------------------------------------------------------------------
    // Remove
    //--------------------------------------------------------------------------------------------------------
    //
    // @param array $array
    // @param mixed $keys
    // @param mixed $values
    //
    //--------------------------------------------------------------------------------------------------------
    public function use(array $array, $keys, $values) : array;

    //--------------------------------------------------------------------------------------------------------
    // Remove Last
    //--------------------------------------------------------------------------------------------------------
    //
    // @param array   $array
    // @param numeric $count
    //
    //--------------------------------------------------------------------------------------------------------
    public function last(array $array, int $count = 1, string $type = 'array_pop') : array;

    //--------------------------------------------------------------------------------------------------------
    // Remove First
    //--------------------------------------------------------------------------------------------------------
    //
    // @param array   $array
    // @param numeric $count
    //
    //--------------------------------------------------------------------------------------------------------
    public function first(array $array, int $count = 1) : array;

    //--------------------------------------------------------------------------------------------------------
    // Delete Element
    //--------------------------------------------------------------------------------------------------------
    //
    // @param array $array
    // @param mixed $object
    //
    //--------------------------------------------------------------------------------------------------------
    public function element(array $array, $object) : array;
}
