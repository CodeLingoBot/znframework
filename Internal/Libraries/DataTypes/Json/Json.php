	//----------------------------------------------------------------------------------------------------
	// Encode                                                                 
	//----------------------------------------------------------------------------------------------------
	//
	// @param mixed  $data
	// @param string $type
	//																						  
	//----------------------------------------------------------------------------------------------------
	public function encode($data, String $type = 'unescaped_unicode') : String
	{
		return json_encode($data, \Convert::toConstant($type, 'JSON_'));
	}
	
	//----------------------------------------------------------------------------------------------------
	// Decode                                                                 
	//----------------------------------------------------------------------------------------------------
	//
	// @param string $data
	// @param bool   $array
	// @param int    $length
	//																						  
	//----------------------------------------------------------------------------------------------------
	public function decode(String $data, Bool $array = false, Int $length = 512)
	{
		return json_decode($data, $array, $length);
	}
	
	//----------------------------------------------------------------------------------------------------
	// Decode Object                                                                 
	//----------------------------------------------------------------------------------------------------
	//
	// @param string $data
	// @param int    $length
	//																						  
	//----------------------------------------------------------------------------------------------------
	public function decodeObject(String $data, Int $length = 512)
	{
		return json_decode($data, false, $length);
	}
	
	//----------------------------------------------------------------------------------------------------
	// Decode Array                                                                 
	//----------------------------------------------------------------------------------------------------
	//
	// @param string $data
	// @param int    $length
	//																						  
	//----------------------------------------------------------------------------------------------------	
	public function decodeArray(String $data, Int $length = 512) : Array
	{	
		return json_decode($data, true, $length);
	}
	
	//----------------------------------------------------------------------------------------------------
	// Error                                                                
	//----------------------------------------------------------------------------------------------------
	//
	// @param void
	//																						  
	//----------------------------------------------------------------------------------------------------	
	public function error() : String
	{
		return json_last_error_msg();
	}
	
	//----------------------------------------------------------------------------------------------------
	// Errval                                                               
	//----------------------------------------------------------------------------------------------------
	//
	// @param void
	//																						  
	//----------------------------------------------------------------------------------------------------	
	public function errval() : String
	{
		return json_last_error_msg();
	}
	
	//----------------------------------------------------------------------------------------------------
	// Errno                                                                
	//----------------------------------------------------------------------------------------------------
	//
	// @param void
	//																						  
	//----------------------------------------------------------------------------------------------------	
	public function errno() : Int
	{
		return json_last_error();
	}
}