    //--------------------------------------------------------------------------------------------------------
    // Key
    //--------------------------------------------------------------------------------------------------------
    //
    // @var string
    //
    //--------------------------------------------------------------------------------------------------------
    protected $key = "+-?||?-+" ;

    //--------------------------------------------------------------------------------------------------------
    // Separator
    //--------------------------------------------------------------------------------------------------------
    //
    // @var string
    //
    //--------------------------------------------------------------------------------------------------------
    protected $separator = "|?-++-?|";

    //--------------------------------------------------------------------------------------------------------
    // Protected Security
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string  $data
    //
    //--------------------------------------------------------------------------------------------------------
    protected function _security($data)
    {
        return str_replace([$this->key, $this->separator], '', $data);
    }
}
