    //--------------------------------------------------------------------------------------------------------

    //--------------------------------------------------------------------------------------------------------
    // To Array
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $string
    // @param string $split
    //
    //--------------------------------------------------------------------------------------------------------
    public function array(string $string, string $split = ' ') :array
    {
        return explode($split, $string);
    }

    //--------------------------------------------------------------------------------------------------------
    // To Char
    //--------------------------------------------------------------------------------------------------------
    //
    // @param int $ascii
    //
    //--------------------------------------------------------------------------------------------------------
    public function char(int $ascii) : string
    {
        return chr($ascii);
    }

    //--------------------------------------------------------------------------------------------------------
    // To Ascii
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $str
    //
    //--------------------------------------------------------------------------------------------------------
    public function ascii(string $string) : int
    {
        return ord($string);
    }
}
