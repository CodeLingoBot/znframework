    // @param mixed $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function bool($var) : bool;

    //--------------------------------------------------------------------------------------------------------
    // Boolean
    //--------------------------------------------------------------------------------------------------------
    //
    // @param mixed $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function boolean($var) : bool;

    //--------------------------------------------------------------------------------------------------------
    // Float
    //--------------------------------------------------------------------------------------------------------
    //
    // @param mixed $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function float($var) : float;

    //--------------------------------------------------------------------------------------------------------
    // Double
    //--------------------------------------------------------------------------------------------------------
    //
    // @param mixed $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function double($var) : float;

    //--------------------------------------------------------------------------------------------------------
