<?php namespace ZN\DataTypes\XML;

use File;

class Save implements SaveInterface
{
    //--------------------------------------------------------------------------------------------------------
    // Extension
    //--------------------------------------------------------------------------------------------------------
    //
    // @var string
    //
    //--------------------------------------------------------------------------------------------------------
    protected $extension = '.xml';

    //--------------------------------------------------------------------------------------------------------
    // Save
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $file
    // @param string $data
    //
    //--------------------------------------------------------------------------------------------------------
    public function do(string $file, string $data) : bool
    {
        $file = suffix($file, $this->extension);

        return File::write($file, $data);
    }
}
