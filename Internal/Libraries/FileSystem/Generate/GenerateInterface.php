	//----------------------------------------------------------------------------------------------------
	// 
	// @param string $name: empty
	//
	//----------------------------------------------------------------------------------------------------
	public function model($name);
	
	//----------------------------------------------------------------------------------------------------
	// Controller
	//----------------------------------------------------------------------------------------------------
	// 
	// @param string $name: empty
	//
	//----------------------------------------------------------------------------------------------------
	public function controller($name);
	
	//----------------------------------------------------------------------------------------------------
	// Library
	//----------------------------------------------------------------------------------------------------
	// 
	// @param string $name: empty
	//
	//----------------------------------------------------------------------------------------------------
