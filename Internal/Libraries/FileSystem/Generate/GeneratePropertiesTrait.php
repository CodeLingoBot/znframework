<?php
namespace ZN\FileSystem;

trait GeneratePropertiesTrait
{
	//----------------------------------------------------------------------------------------------------
	// Application
	//----------------------------------------------------------------------------------------------------
	// 
	// @var string: empty
	//
	//----------------------------------------------------------------------------------------------------
	protected $application;
	
	//----------------------------------------------------------------------------------------------------
	// Extend
	//----------------------------------------------------------------------------------------------------
	// 
	// @var string: empty
	//
	//----------------------------------------------------------------------------------------------------
	protected $extend;
	
	//----------------------------------------------------------------------------------------------------
	// Functions
	//----------------------------------------------------------------------------------------------------
	// 
	// @var string ...args
	//
	//----------------------------------------------------------------------------------------------------
	protected $functions;
	
	//----------------------------------------------------------------------------------------------------
	// Using
	//----------------------------------------------------------------------------------------------------
	// 
	// @var array
	//
	//----------------------------------------------------------------------------------------------------
	protected $using;
	
	//----------------------------------------------------------------------------------------------------
	// Namespaces
	//----------------------------------------------------------------------------------------------------
	// 
	// @var string ...args
	//
	//----------------------------------------------------------------------------------------------------
	protected $namespaces;
	
	
	//----------------------------------------------------------------------------------------------------
	// Using
	//----------------------------------------------------------------------------------------------------
	// 
	// @param string $namespace: empty
	// @param array  $use      : empty
	//
	//----------------------------------------------------------------------------------------------------
	public function using($namespace = '', $using = [])
	{
		$this->using      = $using;
		$this->namespaces = $namespace;
		
		return $this;
	}
	
	//----------------------------------------------------------------------------------------------------
	// Application
	//----------------------------------------------------------------------------------------------------
	// 
	// @param string $name: empty
	//
	//----------------------------------------------------------------------------------------------------
	public function application($name = '')
	{
		$this->application = $name;
		
		return $this;
	}
	
	//----------------------------------------------------------------------------------------------------
	// Extend
	//----------------------------------------------------------------------------------------------------
	// 
	// @param string $name: empty
	//
	//----------------------------------------------------------------------------------------------------
	public function extend($name = '')
	{
		$this->extend = $name;
		
		return $this;
	}
	
	//----------------------------------------------------------------------------------------------------
	// Functions
	//----------------------------------------------------------------------------------------------------
	// 
	// @param array ...$args
	//
	//----------------------------------------------------------------------------------------------------
	public function functions(...$args)
	{
		$this->functions = $args;
		
		return $this;
	}
}