	//----------------------------------------------------------------------------------------------------
	//
	// @param string $table      : empty
	// @param string $otherColumn: empty
	// @param string $operator   : empty
	//
	//----------------------------------------------------------------------------------------------------
	public function outerJoin($table, $otherColumn, $operator);
	
	//----------------------------------------------------------------------------------------------------
	// Left Join
	//----------------------------------------------------------------------------------------------------
	//
	// @param string $table      : empty
	// @param string $otherColumn: empty
	// @param string $operator   : empty
	//
	//----------------------------------------------------------------------------------------------------
	public function leftJoin($table, $otherColumn, $operator);
	
	//----------------------------------------------------------------------------------------------------
	// Right Join
	//----------------------------------------------------------------------------------------------------
	//
	// @param string $table      : empty
	// @param string $otherColumn: empty
	// @param string $operator   : empty
	//
	//----------------------------------------------------------------------------------------------------
