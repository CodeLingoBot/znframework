	//----------------------------------------------------------------------------------------------------
	//
	// @param string $column : empty
	// @param string $value  : empty
	// @param string $logical: empty 
	//
	//----------------------------------------------------------------------------------------------------
	public function having($column, $value, $logical);
	
	//----------------------------------------------------------------------------------------------------
	// Where Group
	//----------------------------------------------------------------------------------------------------
	//
	// @param array ...$args
	//
	//----------------------------------------------------------------------------------------------------
	public function whereGroup();
	
	//----------------------------------------------------------------------------------------------------
	// Having Group
	//----------------------------------------------------------------------------------------------------
	//
	// @param array ...$args
	//
	//----------------------------------------------------------------------------------------------------
	public function havingGroup();
	
