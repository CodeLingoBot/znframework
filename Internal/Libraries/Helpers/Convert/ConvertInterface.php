	//----------------------------------------------------------------------------------------------------
	// Anchor
	//----------------------------------------------------------------------------------------------------
	// 
	// @param string $data
	// @param string $type: short, long
	// @param array  $attributes
	//
	//----------------------------------------------------------------------------------------------------
	public function anchor(String $data, String $type, Array $attributes);
	
	//----------------------------------------------------------------------------------------------------
	// Char
	//----------------------------------------------------------------------------------------------------
	// 
	// @param string $string
	// @param string $type      : char, dec, hex, html
	// @param string $changeType: char, dec, hex, html
	//
	//----------------------------------------------------------------------------------------------------
	public function char(String $string, String $type, String $changeType);

	//----------------------------------------------------------------------------------------------------
	// Accent
	//----------------------------------------------------------------------------------------------------
	// 
	// @param string $str
	//
	//----------------------------------------------------------------------------------------------------
	public function accent(String $str);

	//----------------------------------------------------------------------------------------------------
	// Url Word
	//----------------------------------------------------------------------------------------------------
	// 
	// @param string $str
	// @param string $splitWord
	//
	//----------------------------------------------------------------------------------------------------
	public function urlWord(String $str, String $splitWord);

	//----------------------------------------------------------------------------------------------------
	// String Case
	//----------------------------------------------------------------------------------------------------
	// 
	// @param string $str
	// @param string $type
	// @param string $encoding
	//
	//----------------------------------------------------------------------------------------------------
	public function stringCase(String $str, String $type, String $encoding);	
	
	//----------------------------------------------------------------------------------------------------
	// Array Case
	//----------------------------------------------------------------------------------------------------
	// 
	// @param array  $array
	// @param string $type  : lower, upper, title
	// @param string $keyval: key, val, value, all
	//
	//----------------------------------------------------------------------------------------------------
	public function arrayCase(Array $array, String $type, String $keyval);
	
	//----------------------------------------------------------------------------------------------------
	// Charset
	//----------------------------------------------------------------------------------------------------
	// 
	// @param string $str
	// @param string $fromCharset
	// @param string $toCharset
	//
	//----------------------------------------------------------------------------------------------------
	public function charset(String $str, String $fromCharset, String $toCharset);
	
	//----------------------------------------------------------------------------------------------------
	// High Light
	//----------------------------------------------------------------------------------------------------
	// 
	// @param string $str
	// @param array $settings
	//
	//----------------------------------------------------------------------------------------------------
	public function highLight(String $str, Array $settings);
	
	//----------------------------------------------------------------------------------------------------
	// To Int
	//----------------------------------------------------------------------------------------------------
	// 
	// @param var $var
	//
	//----------------------------------------------------------------------------------------------------
	public function toInt($var);
	
	//----------------------------------------------------------------------------------------------------
	// To Integer
	//----------------------------------------------------------------------------------------------------
	// 
	// @param var $var
	//
	//----------------------------------------------------------------------------------------------------
	public function toInteger($var);
	
	//----------------------------------------------------------------------------------------------------
	// To Bool
	//----------------------------------------------------------------------------------------------------
	// 
	// @param var $var
	//
	//----------------------------------------------------------------------------------------------------
	public function toBool($var);
	
	//----------------------------------------------------------------------------------------------------
	// To Boolean
	//----------------------------------------------------------------------------------------------------
	// 
	// @param var $var
	//
	//----------------------------------------------------------------------------------------------------
	public function toBoolean($var);
	
	//----------------------------------------------------------------------------------------------------
	// To String
	//----------------------------------------------------------------------------------------------------
	// 
	// @param var $var
	//
	//----------------------------------------------------------------------------------------------------
	public function toString($var);
	
	//----------------------------------------------------------------------------------------------------
	// To Float
	//----------------------------------------------------------------------------------------------------
	// 
	// @param var $var
	//
	//----------------------------------------------------------------------------------------------------
	public function toFloat($var);
	
	//----------------------------------------------------------------------------------------------------
	// To Real
	//----------------------------------------------------------------------------------------------------
	// 
	// @param var $var
	//
	//----------------------------------------------------------------------------------------------------
	public function toReal($var);
	
	//----------------------------------------------------------------------------------------------------
	// To Double
	//----------------------------------------------------------------------------------------------------
	// 
	// @param var $var
	//
	//----------------------------------------------------------------------------------------------------
	public function toDouble($var);
	
	//----------------------------------------------------------------------------------------------------
	// To Object
	//----------------------------------------------------------------------------------------------------
	// 
	// @param var $var
	//
	//----------------------------------------------------------------------------------------------------
	public function toObject($var);
	
	//----------------------------------------------------------------------------------------------------
	// To Array
	//----------------------------------------------------------------------------------------------------
	// 
	// @param var $var
	//
	//----------------------------------------------------------------------------------------------------
	public function toArray($var);
	
	//----------------------------------------------------------------------------------------------------
	// To Unset
	//----------------------------------------------------------------------------------------------------
	// 
	// @param var $var
	//
	//----------------------------------------------------------------------------------------------------
	public function toUnset($var);
	
	//----------------------------------------------------------------------------------------------------
	// To Constant
	//----------------------------------------------------------------------------------------------------
	// 
	// @param var    $var
	// @param string $prefix
	// @param string $suffix
	//
	//----------------------------------------------------------------------------------------------------
	public function toConstant($var, String $prefix, String $suffix);
	
	//----------------------------------------------------------------------------------------------------
	// To Unset
	//----------------------------------------------------------------------------------------------------
	// 
	// @param var    $var
	// @param string $type
	//
	//----------------------------------------------------------------------------------------------------
	public function varType($var, String $type);
}