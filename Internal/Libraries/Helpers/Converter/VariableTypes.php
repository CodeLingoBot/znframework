    //--------------------------------------------------------------------------------------------------------

    //--------------------------------------------------------------------------------------------------------
    // To Int
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toInt($var) : int
    {
        return (int) $var;
    }

    //--------------------------------------------------------------------------------------------------------
    // To Integer
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toInteger($var) : int
    {
        return (int) $var;
    }

    //--------------------------------------------------------------------------------------------------------
    // To Bool
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toBool($var) : bool
    {
        return (bool) $var;
    }

    //--------------------------------------------------------------------------------------------------------
    // To Boolean
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toBoolean($var) : bool
    {
        return (bool) $var;
    }

    //--------------------------------------------------------------------------------------------------------
    // To String
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toString($var) : string
    {
        if( is_array($var) || is_object($var) )
        {
            return implode(' ', (array) $var);
        }

        return (string) $var;
    }

    //--------------------------------------------------------------------------------------------------------
    // To Float
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toFloat($var) : float
    {
        return (float) $var;
    }

    //--------------------------------------------------------------------------------------------------------
    // To Real
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toReal($var)
    {
        return (real) $var;
    }

    //--------------------------------------------------------------------------------------------------------
    // To Double
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toDouble($var)
    {
        return (double) $var;
    }

    //--------------------------------------------------------------------------------------------------------
    // To Object
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toObject($var) : \stdClass
    {
        return (object) $var;
    }

    //--------------------------------------------------------------------------------------------------------
    // To Array
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toArray($var) : array
    {
        return (array) $var;
    }

    //--------------------------------------------------------------------------------------------------------
    // To Constant
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $var
    // @param string $prefix
    // @param string $suffix
    //
    //--------------------------------------------------------------------------------------------------------
    public function toConstant(string $var, string $prefix = NULL, string $suffix = NULL)
    {
        if( defined(strtoupper($prefix.$var.$suffix)) )
        {
            return constant(strtoupper($prefix.$var.$suffix));
        }
        elseif( defined($var) )
        {
            return constant($var);
        }
        else
        {
            return $var;
        }
    }
}
