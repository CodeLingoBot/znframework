    //--------------------------------------------------------------------------------------------------------
    // File
    //--------------------------------------------------------------------------------------------------------
    //
    // @param  string $file
    // @return content
    //
    //--------------------------------------------------------------------------------------------------------
    public function file(String $file) : String
    {
        return Buffer\File::do($file);
    }

    //--------------------------------------------------------------------------------------------------------
    // Func
    //--------------------------------------------------------------------------------------------------------
    //
    // @param  string/callable $func
    // @param  array           $params
    // @return callable
    //
    //--------------------------------------------------------------------------------------------------------
    public function func($func, Array $params = [])
    {
        return Buffer\Callback::do($func, $params);
    }

    //--------------------------------------------------------------------------------------------------------
    // Callback / Func
    //--------------------------------------------------------------------------------------------------------
    //
    // func() yönteminin takma adıdır.
    //
    //--------------------------------------------------------------------------------------------------------
    public function callback($func, Array $params = [])
    {
        return $this->func($func, $params);
    }

    //--------------------------------------------------------------------------------------------------------
    // Insert
    //--------------------------------------------------------------------------------------------------------
    //
    // @param  string                 $name
    // @param  callable/object/string $data
    // @param  array                  $params
    // @return bool
    //
    //--------------------------------------------------------------------------------------------------------
    public function insert(String $name, $data, Array $params = []) : Bool
    {
        return Buffer\Insert::do($name, $data, $params);
    }

    //--------------------------------------------------------------------------------------------------------
    // Select
    //--------------------------------------------------------------------------------------------------------
    //
    // @param  string $name
    // @return callable/content
    //
    //--------------------------------------------------------------------------------------------------------
    public function select(String $name)
    {
        return Buffer\Select::do($name);
    }

    //--------------------------------------------------------------------------------------------------------
    // Delete
    //--------------------------------------------------------------------------------------------------------
    //
    // @param  string $name
    // @return bool
    //
    //--------------------------------------------------------------------------------------------------------
    public function delete($name) : Bool
    {
        return Buffer\Delete::do($name);
    }
}
