	// @param string $theme
	// @param bool   $recursive  
	// @param bool   $getContents     	              
	//          																				  
	//--------------------------------------------------------------------------------------------------------
	public function theme($theme, Bool $recursive = false, Bool $getContents = false) : String;
	
