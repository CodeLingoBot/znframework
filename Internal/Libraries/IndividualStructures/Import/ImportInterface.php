	//--------------------------------------------------------------------------------------------------------
	//
	// @param string $plugin
	// @param bool   $recursive  
	// @param bool   $getContents     	              
	//          																				  
	//--------------------------------------------------------------------------------------------------------
	public function plugin($plugin, Bool $recursive = false, Bool $getContents = false) : String;
