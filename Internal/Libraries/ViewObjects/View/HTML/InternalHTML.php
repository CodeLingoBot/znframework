    //--------------------------------------------------------------------------------------------------------
    // Anchor
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $url
    // @param string $value
    // @param array  $attributes
    //
    //--------------------------------------------------------------------------------------------------------
    public function anchor(string $url, string $value = NULL, array $attributes = []) : string
    {
        if( ! isUrl($url) && strpos($url, '#') !== 0 )
        {
            $url = siteUrl($url);
        }

        $attributes['href'] = $url;

        nullCoalesce($value, $url);

        return $this->_multiElement('a', $value, $attributes);
    }

