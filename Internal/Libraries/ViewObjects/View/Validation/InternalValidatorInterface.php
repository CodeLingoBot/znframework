    //--------------------------------------------------------------------------------------------------------
    // Phone
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $data
    // @param string $phoneData
    //
    //--------------------------------------------------------------------------------------------------------
    public function phone(string $data, string $pattern = NULL) : bool;

    //--------------------------------------------------------------------------------------------------------
    // Numeric
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $data
    //
    //--------------------------------------------------------------------------------------------------------
    public function numeric($data) : bool;

    //--------------------------------------------------------------------------------------------------------
    // Alnum
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $data
    //
    //--------------------------------------------------------------------------------------------------------
    public function alnum(string $data) : bool;

    //--------------------------------------------------------------------------------------------------------
    // Alpha
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $data
    //
    //--------------------------------------------------------------------------------------------------------
    public function alpha(String $data) : Bool;

