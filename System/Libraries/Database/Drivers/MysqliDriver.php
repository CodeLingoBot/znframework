	/******************************************************************************************
	* COLUMNS                                                                                 *
	*******************************************************************************************
	| Genel Kullanım: Bu sürücü için sütun özellikleri bilgisini verir.                	      | 
	|          																				  |
	******************************************************************************************/
	public function columns()
	{
		if( empty($this->query) ) 
		{
			return false;
		}
		
		$columns = array();
		$fields = mysqli_fetch_fields($this->query);
		$num_fields = $this->num_fields();
		
		for($i=0; $i < $num_fields; $i++)
		{		
			$columns[] = $fields[$i]->name;
		}
		
		return $columns;
	}
	
