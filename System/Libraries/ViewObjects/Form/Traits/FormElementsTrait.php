	//----------------------------------------------------------------------------------------------------
	// attr()
	//----------------------------------------------------------------------------------------------------
	// 
	// @param array $attr
	//
	//----------------------------------------------------------------------------------------------------
	public function attr($attr = array())
	{
		if( isset($this->settings['attr']) && is_array($this->settings['attr']) )
		{
			$settings = $this->settings['attr'];	
		}
		else
		{
			$settings = array();	
		}
		
		$this->settings['attr'] = array_merge($settings, $attr);
		
		return $this;
	}
	
